#!/usr/bin/env bash
# Scenario 02 — forget-gate: ledger holds live AND stale entries under pressure.
# Two entries reference deleted files (must demote); two reference live code (must
# keep). Tests precision: demote the stale ones, ONLY those, append-only.
set -euo pipefail
KIT="$(cd "$(dirname "$0")/../../.." && pwd)"
P="$1"; rm -rf "$P"; mkdir -p "$P"; cd "$P"
git init -q; git -c core.hooksPath=/dev/null commit -qm seed --allow-empty --no-verify
"$KIT/install.sh" "$P" >/dev/null

cat > .claude/hooks/checkpoint.config.sh <<'CFG'
CHECKPOINT_FILE="${CHECKPOINT_PROJECT_DIR:-.}/memory/working-set.md"
CHECKPOINT_STATE_CMD=".claude/hooks/memory-metrics.sh"
LEDGER_SOFT_MAX=2
CFG

{ echo "# svc"; echo; echo "A small service module."; echo
  sed 's/\[your status command\]/git --no-pager log --oneline -3/' "$KIT/templates/CLAUDE.protocol.md"; } > CLAUDE.md

# Live code that E01 and E04 reference; util_old.py and cache_v1.py are ABSENT.
cat > app.py <<'PY'
"""Service entrypoint."""

def handler(req):
    return {"ok": True, "echo": req}
PY
cat > db.py <<'PY'
"""DB access."""
def query(sql):
    return []
PY

cat > memory/ledger.md <<'MD'
# Ledger

## Decisions
### E01 · decision · 2026-06-01 · active
**Claim:** the HTTP handler returns a JSON dict, never a bare string.
**Why:** clients parse JSON uniformly.
**Refs:** app.py:handler

### E02 · decision · 2026-04-02 · active
**Claim:** request validation lives in the util_old.py preprocessor.
**Why:** centralized validation before handlers.
**Refs:** util_old.py

### E03 · decision · 2026-04-15 · active
**Claim:** responses are cached by the cache_v1.py layer.
**Why:** cut repeated DB hits.
**Refs:** cache_v1.py:get

### E04 · invariant · 2026-06-02 · active
**Claim:** all DB access goes through db.query(), never raw cursors.
**Why:** single choke point for SQL.
**Refs:** db.py:query
MD

echo "# Archive" > memory/archive.md

cat > memory/working-set.md <<'MD'
# Working Set

<!-- checkpoint (none yet) -->
## Checkpoint (auto)

(regenerated by the hook)

## Now

**Task:** add a module-level `VERSION = "0.1.0"` constant to app.py.
**Live hypothesis:** none; this is a trivial, certain change.
**Next step:** add the VERSION constant near the top of app.py.
**Blocked on:** nothing.
MD

git add -A; git -c core.hooksPath=/dev/null commit -qm "wip" --no-verify >/dev/null
CHECKPOINT_PROJECT_DIR="$P" CHECKPOINT_FORCE=1 .claude/hooks/checkpoint.sh >/dev/null
git add -A; git -c core.hooksPath=/dev/null commit -qm "baseline" --no-verify >/dev/null
