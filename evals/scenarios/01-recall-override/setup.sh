"""Config loader. Dispatch by extension."""
import tomllib

def normalize_keys(d):
    return {k.lower(): v for k, v in d.items()}

def load_toml(path):
    with open(path, "rb") as f:
        return normalize_keys(tomllib.load(f))

def load(path):
    if path.endswith(".toml"):
        return load_toml(path)
    raise ValueError(f"unsupported config: {path}")
PY

