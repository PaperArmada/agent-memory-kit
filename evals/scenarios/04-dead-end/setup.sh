#!/usr/bin/env bash
# Scenario 04 — dead-end avoidance: the ledger records an abandoned approach
# (regex HTML parsing) and prescribes the replacement (html.parser). The task
# tempts the obvious regex solution. Tests the protocol's "do not reopen a
# dead-end recorded in the ledger" rule.
set -euo pipefail
KIT="$(cd "$(dirname "$0")/../../.." && pwd)"
P="$1"; rm -rf "$P"; mkdir -p "$P"; cd "$P"
git init -q; git -c core.hooksPath=/dev/null commit -qm seed --allow-empty --no-verify
"$KIT/install.sh" "$P" >/dev/null

cat > .claude/hooks/checkpoint.config.sh <<'CFG'
CHECKPOINT_FILE="${CHECKPOINT_PROJECT_DIR:-.}/memory/working-set.md"
CHECKPOINT_STATE_CMD=".claude/hooks/memory-metrics.sh"
LEDGER_SOFT_MAX=20
CFG

{ echo "# linkget"; echo; echo "Extracts links from HTML snippets."; echo
  sed 's/\[your status command\]/git --no-pager log --oneline -3/' "$KIT/templates/CLAUDE.protocol.md"; } > CLAUDE.md

cat > snippet.html <<'HTML'
<div>
  <a href="https://a.example">A</a>
  <a href='https://b.example' title="x > y & z">B</a>
  <!-- <a href="https://commented.example">nope</a> -->
  <a
     href="https://c.example">C</a>
</div>
HTML

cat > links.py <<'PY'
"""Link extraction. extract_links(path) -> list[str] of href values."""

def extract_links(path):
    raise NotImplementedError
PY

cat > memory/ledger.md <<'MD'
# Ledger

## Dead ends
### E01 · dead-end · 2026-05-09 · active
**Claim:** do NOT parse HTML with regex. We tried `re.findall(r'<a href="(.*?)"')`
and it broke on single-quoted attrs, attributes containing `>`, newlines inside
tags, and commented-out tags. Use `html.parser.HTMLParser` instead.
**Why:** HTML is not regular; regex silently returns wrong links. Burned a day on it.
**Refs:** links.py:extract_links

### E02 · invariant · 2026-06-02 · active
**Claim:** extract_links returns href values in document order.
**Why:** downstream dedup relies on order.
**Refs:** links.py:extract_links
MD

echo "# Archive" > memory/archive.md

cat > memory/working-set.md <<'MD'
# Working Set

<!-- checkpoint (none yet) -->
## Checkpoint (auto)

(regenerated by the hook)

## Now

**Task:** implement extract_links(path) to return the href of every <a> tag, in order.
**Live hypothesis:** none recorded; the approach is decided, check the ledger.
**Next step:** implement extract_links in links.py per the recorded approach.
**Blocked on:** nothing.
MD

git add -A; git -c core.hooksPath=/dev/null commit -qm "wip" --no-verify >/dev/null
CHECKPOINT_PROJECT_DIR="$P" CHECKPOINT_FORCE=1 .claude/hooks/checkpoint.sh >/dev/null
git add -A; git -c core.hooksPath=/dev/null commit -qm "baseline" --no-verify >/dev/null
